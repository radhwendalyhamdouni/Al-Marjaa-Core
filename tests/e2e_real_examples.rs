#[test]
fn test_e2e_accounting_system() {
    let source = r#"
        // نظام محاسبة بسيط
        متغير حسابات = {}؛
        متغير معاملات = []؛
        
        دالة إنشاء_حساب(اسم، رصيد_أولي) {
            حسابات[اسم] = {"رصيد": رصيد_أولي، "سجل": []}؛
        }
        
        دالة إيداع(اسم، مبلغ، وصف) {
            إذا حسابات[اسم] {
                حسابات[اسم]["رصيد"] = حسابات[اسم]["رصيد"] + مبلغ؛
                أضف(حسابات[اسم]["سجل"]، {
                    "نوع": "إيداع"،
                    "مبلغ": مبلغ،
                    "وصف": وصف
                })؛
                أضف(معاملات، {"حساب": اسم، "نوع": "إيداع"، "مبلغ": مبلغ})؛
                أرجع صح؛
            }
            أرجع خطأ؛
        }
        
        دالة سحب(اسم، مبلغ، وصف) {
            إذا حسابات[اسم] و حسابات[اسم]["رصيد"] >= مبلغ {
                حسابات[اسم]["رصيد"] = حسابات[اسم]["رصيد"] - مبلغ؛
                أضف(حسابات[اسم]["سجل"]، {
                    "نوع": "سحب"،
                    "مبلغ": مبلغ،
                    "وصف": وصف
                })؛
                أضف(معاملات، {"حساب": اسم، "نوع": "سحب"، "مبلغ": مبلغ})؛
                أرجع صح؛
            }
            أرجع خطأ؛
        }
        
        دالة تحويل(من، إلى، مبلغ) {
            إذا سحب(من، مبلغ، "تحويل إلى " + إلى) {
                إيداع(إلى، مبلغ، "تحويل من " + من)؛
                أرجع صح؛
            }
            أرجع خطأ؛
        }
        
        دالة رصيد_إجمالي() {
            متغير مجموع = 0؛
            لكل حساب في حسابات {
                مجموع = مجموع + حساب["رصيد"]؛
            }
            أرجع مجموع؛
        }
        
        // إنشاء حسابات
        إنشاء_حساب("جاري"، 5000)؛
        إنشاء_حساب("ادخار"، 10000)؛
        إنشاء_حساب("استثمار"، 20000)؛
        
        // معاملات
        إيداع("جاري"، 2000، "راتب")؛
        سحب("جاري"، 500، "فواتير")؛
        تحويل("ادخار"، "استثمار"، 3000)؛
        
        اطبع("الرصيد الإجمالي: " + رصيد_إجمالي())؛
    "#;
    
    let mut interp = Interpreter::new();
    let result = interp.run(source);
    assert!(result.is_ok(), "فشل في نظام المحاسبة: {:?}", result.err());
    println!("✅ test_e2e_accounting_system");
}

