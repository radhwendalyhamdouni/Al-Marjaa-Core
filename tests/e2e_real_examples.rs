// اختبار E2E مع JIT
#[test]
fn test_e2e_jit_large_program() {
    let source = r#"
        // برنامج كبير لاختبار JIT
        متغير نتائج = []؛
        
        دالة حساب_مضروب(ن) {
            إذا ن <= 1 {
                أرجع 1؛
            }
            أرجع ن * حساب_مضروب(ن - 1)؛
        }
        
        دالة تحقق_أولي(ن) {
            إذا ن < 2 {
                أرجع خطأ؛
            }
            لكل س في مدى(2، ن) {
                إذا ن % س == 0 {
                    أرجع خطأ؛
                }
            }
            أرجع صح؛
        }
        
        // عمليات متعددة
        لكل س في مدى(1، 16) {
            أضف(نتائج، حساب_مضروب(س))؛
        }
        
        متغير أوليات = []؛
        لكل س في مدى(2، 51) {
            إذا تحقق_أولي(س) {
                أضف(أوليات، س)؛
            }
        }
        
        اطبع("المضروبات: " + نتائج)؛
        اطبع("الأعداد الأولية: " + أوليات)؛
    "#;
    
    let chunk = Compiler::compile_source(source).expect("فشل Compiler");
    let mut jit = CompleteV2JitCompiler::new();
    let mut globals = Rc::new(RefCell::new(Environment::new()));
    
    let start = std::time::Instant::now();
    let result = jit.execute(&chunk, &mut globals);
    let elapsed = start.elapsed();
    
    assert!(result.is_ok(), "فشل JIT: {:?}", result.err());
    
    let stats = jit.stats();
    println!("✅ test_e2e_jit_large_program");
    println!("   الوقت: {:?}", elapsed);
    println!("   التنفيذات: {}", stats.total_executions);
    println!("   الاستدعاءات العودية: {}", stats.recursive_calls);
}
