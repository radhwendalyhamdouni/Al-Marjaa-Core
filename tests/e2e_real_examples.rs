#[test]
fn test_e2e_flight_booking() {
    let source = r#"
        // نظام حجز تذاكر طيران
        متغير رحلات = [
            {"رقم": "RA101"، "من": "الرياض"، "إلى": "جدة"، "مقاعد": 150، "سعر": 350}،
            {"رقم": "RA102"، "من": "الرياض"، "إلى": "دبي"، "مقاعد": 200، "سعر": 800}،
            {"رقم": "RA103"، "من": "جدة"، "إلى": "القاهرة"، "مقاعد": 180، "سعر": 600}
        ]؛
        
        متغير حجوزات = []؛
        
        دالة بحث_رحلة(من، إلى) {
            متغير نتائج = []؛
            لكل رحلة في رحلات {
                إذا رحلة["من"] == من و رحلة["إلى"] == إلى {
                    أضف(نتائج، رحلة)؛
                }
            }
            أرجع نتائج؛
        }
        
        دالة حجز_رحلة(رقم_الرحلة، اسم_المسافر) {
            لكل رحلة في رحلات {
                إذا رحلة["رقم"] == رقم_الرحلة و رحلة["مقاعد"] > 0 {
                    رحلة["مقاعد"] = رحلة["مقاعد"] - 1؛
                    أضف(حجوزات، {
                        "رقم_الحجز": طول(حجوزات) + 1،
                        "رقم_الرحلة": رقم_الرحلة،
                        "المسافر": اسم_المسافر،
                        "السعر": رحلة["سعر"]
                    })؛
                    أرجع "تم الحجز بنجاح"؛
                }
            }
            أرجع "لا توجد مقاعد متاحة"؛
        }
        
        دالة إلغاء_حجز(رقم_الحجز) {
            لكل حجز في حجوزات {
                إذا حجز["رقم_الحجز"] == رقم_الحجز {
                    لكل رحلة في رحلات {
                        إذا رحلة["رقم"] == حجز["رقم_الرحلة"] {
                            رحلة["مقاعد"] = رحلة["مقاعد"] + 1؛
                        }
                    }
                }
            }
            أرجع "تم إلغاء الحجز"؛
        }
        
        // البحث والحجز
        متغيرة نتائج = بحث_رحلة("الرياض"، "جدة")؛
        اطبع("رحلات متاحة: " + نتائج)؛
        
        حجز_رحلة("RA101"، "أحمد محمد")؛
        حجز_رحلة("RA102"، "سارة أحمد")؛
        
        اطبع("عدد الحجوزات: " + طول(حجوزات))؛
    "#;
    
    let mut interp = Interpreter::new();
    let result = interp.run(source);
    assert!(result.is_ok(), "فشل في نظام حجز الطيران: {:?}", result.err());
    println!("✅ test_e2e_flight_booking");
}

