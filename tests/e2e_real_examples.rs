#[test]
fn test_e2e_advanced_algorithms() {
    let source = r#"
        // خوارزميات متقدمة
        
        // ترتيب سريع (Quick Sort)
        دالة ترتيب_سريع(قائمة) {
            إذا طول(قائمة) <= 1 {
                أرجع قائمة؛
            }
            
            متوسط محور = قائمة[0]؛
            متوسط أصغر = []؛
            متوسط أكبر = []؛
            
            لكل س في مدى(1، طول(قائمة)) {
                إذا قائمة[س] < محور {
                    أضف(أصغر، قائمة[س])؛
                }
                وإلا {
                    أضف(أكبر، قائمة[س])؛
                }
            }
            
            // دمج النتائج
            أرجع ترتيب_سريع(أصغر) + [محور] + ترتيب_سريع(أكبر)؛
        }
        
        // بحث ثنائي
        دالة بحث_ثنائي(قائمة، هدف) {
            متوسط يسار = 0؛
            متوسط يمين = طول(قائمة) - 1؛
            
            طالما يسار <= يمين {
                متوسط وسط = (يسار + يمين) / 2؛
                
                إذا قائمة[وسط] == هدف {
                    أرجع وسط؛
                }
                وإلا إذا قائمة[وسط] < هدف {
                    يسار = وسط + 1؛
                }
                وإلا {
                    يمين = وسط - 1؛
                }
            }
            أرجع -1؛
        }
        
        // حساب Fibonacci بكفاءة
        دالة فيبوناتشي(ن) {
            إذا ن <= 1 {
                أرجع ن؛
            }
            
            متوسط أ = 0؛
            متوسط ب = 1؛
            
            لكل س في مدى(2، ن + 1) {
                متوسط مؤقت = أ + ب؛
                أ = ب؛
                ب = مؤقت؛
            }
            أرجع ب؛
        }
        
        // اختبار الترتيب
        متوسط أرقام = [64، 34، 25، 12، 22، 11، 90]؛
        متوسط مرتبة = ترتيب_سريع(أرقام)؛
        اطبع("مرتبة: " + مرتبة)؛
        
        // اختبار البحث
        متوسط فهرس = بحث_ثنائي(مرتبة، 25)؛
        اطبع("فهرس 25: " + فهرس)؛
        
        // اختبار Fibonacci
        اطبع("Fibonacci 20: " + فيبوناتشي(20))؛
    "#;
    
    let mut interp = Interpreter::new();
    let result = interp.run(source);
    assert!(result.is_ok(), "فشل في الخوارزميات المتقدمة: {:?}", result.err());
    println!("✅ test_e2e_advanced_algorithms");
}

