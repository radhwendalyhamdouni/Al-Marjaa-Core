#[test]
fn test_e2e_text_analysis() {
    let source = r#"
        // نظام تحليل نصوص
        متغير نص = "مرحباً بالعالم! هذه تجربة لتحليل النصوص العربية. العربية لغة جميلة."؛
        
        دالة عدد_الكلمات(نص) {
            متوسط عدد = 1؛
            لكل حرف في نص {
                إذا حرف == " " {
                    عدد = عدد + 1؛
                }
            }
            أرجع عدد؛
        }
        
        دالة عدد_الجمل(نص) {
            متوسط عدد = 0؛
            لكل حرف في نص {
                إذا حرف == "!" أو حرف == "؟" أو حرف == "." {
                    عدد = عدد + 1؛
                }
            }
            أرجع عدد؛
        }
        
        دالة تكرار_الحروف(نص) {
            متوسط تكرار = {}؛
            لكل حرف في نص {
                إذا حرف != " " و حرف != "!" و حرف != "؟" و حرف != "." {
                    إذا تكرار[حرف] {
                        تكرار[حرف] = تكرار[حرف] + 1؛
                    }
                    وإلا {
                        تكرار[حرف] = 1؛
                    }
                }
            }
            أرجع تكرار؛
        }
        
        دالة أكثر_حرف_تكراراً(نص) {
            متوسط تكرار = تكرار_الحروف(نص)؛
            متوسط أعلى = 0؛
            متوسط الحرف = ""؛
            
            لكل ح في تكرار {
                إذا تكرار[ح] > أعلى {
                    أعلى = تكرار[ح]؛
                    الحرف = ح؛
                }
            }
            أرجع {"حرف": الحرف، "تكرار": أعلى}؛
        }
        
        // تحليل النص
        اطبع("عدد الكلمات: " + عدد_الكلمات(نص))؛
        اطبع("عدد الجمل: " + عدد_الجمل(نص))؛
        متوسط الأكثر = أكثر_حرف_تكراراً(نص)؛
        اطبع("أكثر حرف تكراراً: " + الأكثر["حرف"] + " (" + الأكثر["تكرار"] + " مرات)")؛
    "#;
    
    let mut interp = Interpreter::new();
    let result = interp.run(source);
    assert!(result.is_ok(), "فشل في تحليل النصوص: {:?}", result.err());
    println!("✅ test_e2e_text_analysis");
}

