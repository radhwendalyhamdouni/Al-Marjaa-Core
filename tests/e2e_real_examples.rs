#[test]
fn test_e2e_library_management() {
    let source = r#"
        // نظام إدارة مكتبة
        متغير كتب = []؛
        
        دالة أضف_كتاب(عنوان، مؤلف، سنة) {
            أضف(كتب، {
                "عنوان": عنوان،
                "مؤلف": مؤلف،
                "سنة": سنة،
                "معار": خطأ
            })؛
        }
        
        دالة استعارة_كتاب(فهرس) {
            إذا فهرس >= 0 و فهرس < طول(كتب) {
                إذا ليس كتب[فهرس]["معار"] {
                    كتب[فهرس]["معار"] = صح؛
                    أرجع "تمت الاستعارة بنجاح"؛
                }
                أرجع "الكتاب معار بالفعل"؛
            }
            أرجع "فهرس غير صالح"؛
        }
        
        دالة إرجاع_كتاب(فهرس) {
            إذا فهرس >= 0 و فهرس < طول(كتب) {
                كتب[فهرس]["معار"] = خطأ؛
                أرجع "تم الإرجاع بنجاح"؛
            }
            أرجع "فهرس غير صالح"؛
        }
        
        دالة كتب_متاحة() {
            متغير متاحة = []؛
            لكل كتاب في كتب {
                إذا ليس كتاب["معار"] {
                    أضف(متاحة، كتاب["عنوان"])؛
                }
            }
            أرجع متاحة؛
        }
        
        // إضافة كتب
        أضف_كتاب("مقدمة في الخوارزميات"، "أحمد محمد"، 2020)؛
        أضف_كتاب("تعلم البرمجة"، "سارة أحمد"، 2021)؛
        أضف_كتاب("هياكل البيانات"، "محمد علي"، 2019)؛
        أضف_كتاب("الذكاء الاصطناعي"، "فاطمة حسن"، 2022)؛
        
        // استعارة وإرجاع
        استعارة_كتاب(0)؛
        استعارة_كتاب(2)؛
        إرجاع_كتاب(0)؛
        
        // عرض الكتب المتاحة
        متغير متاحة = كتب_متاحة()؛
        اطبع("الكتب المتاحة: " + متاحة)؛
    "#;
    
    let mut interp = Interpreter::new();
    let result = interp.run(source);
    assert!(result.is_ok(), "فشل في نظام المكتبة: {:?}", result.err());
    println!("✅ test_e2e_library_management");
}

